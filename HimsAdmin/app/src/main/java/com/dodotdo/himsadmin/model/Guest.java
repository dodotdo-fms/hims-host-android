public class Guest {
    String type;
    String firstName;
    String middleName;
    String lastName;
    String id;
    String registerTimestamp;

    public String getType() {
        return type;
    }

    public String getFirstName() {
        return firstName;
    }

    public String getMiddleName() {
        return middleName;
    }

    public String getLastName() {
        return lastName;
    }

    public String getId() {
        return id;
    }

    public String getRegisterTimestamp() {
        return registerTimestamp;
    }
}
